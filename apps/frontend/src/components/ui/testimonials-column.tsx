"use client"

import React from "react"
import { motion } from "framer-motion"

export const TestimonialsColumn = (props: {
    className?: string
    testimonials: Array<{
        text: string
        image: string
        name: string
        role: string
    }>
    duration?: number
    reverse?: boolean
}) => {
    return (
        <div className={props.className}>
            <motion.div
                initial={{
                    translateY: props.reverse ? "-50%" : "0%",
                }}
                animate={{
                    translateY: props.reverse ? "0%" : "-50%",
                }}
                transition={{
                    duration: props.duration || 10,
                    repeat: Infinity,
                    ease: "linear",
                    repeatType: "loop",
                }}
                className="flex flex-col gap-6 pb-6"
            >
                {[...new Array(2)].fill(0).map((_, index) => (
                    <React.Fragment key={index}>
                        {props.testimonials.map(({ text, image, name, role }, i) => (
                            <div
                                className="p-10 rounded-3xl border border-border bg-card shadow-lg shadow-primary/10 max-w-xs w-full"
                                key={i}
                            >
                                <div className="text-sm leading-relaxed">{text}</div>
                                <div className="flex items-center gap-2 mt-5">
                                    <img
                                        width={40}
                                        height={40}
                                        src={image}
                                        alt={name}
                                        className="h-10 w-10 rounded-full"
                                    />
                                    <div className="flex flex-col">
                                        <div className="font-medium tracking-tight leading-5">
                                            {name}
                                        </div>
                                        <div className="text-sm leading-5 text-muted-foreground tracking-tight">
                                            {role}
                                        </div>
                                    </div>
                                </div>
                            </div>
                        ))}
                    </React.Fragment>
                ))}
            </motion.div>
        </div>
    )
}
